cherry<-read.csv("cherry.csv")
washtemp<-read.csv("washtemp.csv", head=TRUE, sep='')
frame_cherry<-data.frame(cherry$Year, cherry$Yoshino.peak.bloom.date)
frame_washtemp<-data.frame(washtemp$YEAR, washtemp$MAR)
names(frame_cherry)[1:2]<-c("Year", "BloomDays")
names(frame_washtemp)[1:2]<-c("Year", "MarchTemp")
frameboth<-merge(frame_cherry, frame_washtemp, by = intersect(names(frame_cherry), names(frame_washtemp)), by.x='Year')
plot(frameboth$BloomDays, frameboth$MarchTemp, xlab="Bloom Days", ylab="March Temperature", main="Bloom Days by Temperature in Washington, DC")
abline(lm(frameboth$MarchTemp~frameboth$BloomDays), col="red")