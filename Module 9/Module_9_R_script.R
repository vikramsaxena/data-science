a<-read.csv("summer.csv")
a$ev<-paste(a$Discipline, a$Event, a$Gender, sep="_")
b<-a[c("Year", "ev")]
c<-unique(b)
q<-table(c$Year)
plot(q, xlab="Year", ylab="Events", main="Number of Events at Summer Olympics", type="l")

qa<-as.data.frame(q[11:27])
names(qa)=c("Year", "Event")
qa$Year<-as.numeric(as.character(qa$Year))
plot(qa, xlab="Year", ylab="Events", main="Number of Events at Summer Olympics from 1950 onwards")
li_a<-lm(Event~Year, data=qa)
abline(li_a)
predict(li_a, list(Year=2040)) 

